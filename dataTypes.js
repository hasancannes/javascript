// ? Primitive Veri Tipleri

// * String

let isim = "Hasan";
console.log(typeof isim);
console.log(isim)

// * Number

let sayi = 5;
let sayi2 = 5.5;
console.log(typeof sayi);
console.log(sayi,sayi2)

// * Boolean

let bool = true;
console.log(5<2);
console.log(bool);

// * Null

let a= null;

a= 15;
console.log(a);

// * Undefined

let c;

console.log(typeof c);

// ? Reference Veri Tipleri
// * Object

let insan = {
    isim : "Hasan",
    yas : 25
}
